# 3)Casos de nombre: Almacena el nombre de una persona en una variable y luego imprime ese nombre en minúsculas, mayúsculas y con la primera letra de cada palabra en mayúscula (formato título).
nombre="Lucia"
print(nombre.lower()) #lower() convierte a minúsculas
print(nombre.upper()) #upper() convierte a mayúsculas
print(nombre.title()) #title() convierte a formato título
